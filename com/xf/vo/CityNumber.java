package com.xf.vo;

public class CityNumber {
	private int id;
	private String districtName;
	private int huge;
	private int big;
	private int mid;
	private int small;
	private int shitang;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDistrictName() {
		return districtName;
	}
	public void setDistrictName(String districtName) {
		this.districtName = districtName;
	}
	public int getHuge() {
		return huge;
	}
	public void setHuge(int huge) {
		this.huge = huge;
	}
	public int getBig() {
		return big;
	}
	public void setBig(int big) {
		this.big = big;
	}
	public int getMid() {
		return mid;
	}
	public void setMid(int mid) {
		this.mid = mid;
	}
	public int getSmall() {
		return small;
	}
	public void setSmall(int small) {
		this.small = small;
	}
	public int getShitang() {
		return shitang;
	}
	public void setShitang(int shitang) {
		this.shitang = shitang;
	}
	
}
