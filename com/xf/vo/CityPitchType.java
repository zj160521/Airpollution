package com.xf.vo;

public class CityPitchType {
	private int id;
	private String name;
	private double ksPitch;
	private double zgPitch;
	private double cgPitch;
	private double zPitch;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getKsPitch() {
		return ksPitch;
	}
	public void setKsPitch(double ksPitch) {
		this.ksPitch = ksPitch;
	}
	public double getZgPitch() {
		return zgPitch;
	}
	public void setZgPitch(double zgPitch) {
		this.zgPitch = zgPitch;
	}
	public double getCgPitch() {
		return cgPitch;
	}
	public void setCgPitch(double cgPitch) {
		this.cgPitch = cgPitch;
	}
	public double getzPitch() {
		return zPitch;
	}
	public void setzPitch(double zPitch) {
		this.zPitch = zPitch;
	}
	
}
