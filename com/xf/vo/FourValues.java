package com.xf.vo;

public class FourValues {
	private double zgsum;
	private double cgsum;
	private double zsum;
	private double kssum;
	public double getZgsum() {
		return zgsum;
	}
	public void setZgsum(double zgsum) {
		this.zgsum = zgsum;
	}
	public double getCgsum() {
		return cgsum;
	}
	public void setCgsum(double cgsum) {
		this.cgsum = cgsum;
	}
	public double getZsum() {
		return zsum;
	}
	public void setZsum(double zsum) {
		this.zsum = zsum;
	}
	public double getKssum() {
		return kssum;
	}
	public void setKssum(double kssum) {
		this.kssum = kssum;
	}
	
}
