package com.xf.vo;

public class GisStat {
	
	private int pollutantid;
	private String pollutantName;
	private int countyid;
	private String districtName;
	private Double e_point;
	private Double n_point;
	private Double total;
	
	public int getPollutantid() {
		return pollutantid;
	}
	public void setPollutantid(int pollutantid) {
		this.pollutantid = pollutantid;
	}
	public String getPollutantName() {
		return pollutantName;
	}
	public void setPollutantName(String pollutantName) {
		this.pollutantName = pollutantName;
	}
	public int getCountyid() {
		return countyid;
	}
	public void setCountyid(int countyid) {
		this.countyid = countyid;
	}
	public String getDistrictName() {
		return districtName;
	}
	public void setDistrictName(String districtName) {
		this.districtName = districtName;
	}
	public Double getE_point() {
		return e_point;
	}
	public void setE_point(Double e_point) {
		this.e_point = e_point;
	}
	public Double getN_point() {
		return n_point;
	}
	public void setN_point(Double n_point) {
		this.n_point = n_point;
	}
	public Double getTotal() {
		return total;
	}
	public void setTotal(Double total) {
		this.total = total;
	}
	
		

}
