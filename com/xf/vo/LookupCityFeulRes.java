package com.xf.vo;

public class LookupCityFeulRes {
	private int cityid;
	private String districtName;
	private int feulid;
	private String name;
	private double sum;
	public int getCityid() {
		return cityid;
	}
	public void setCityid(int cityid) {
		this.cityid = cityid;
	}
	public String getDistrictName() {
		return districtName;
	}
	public void setDistrictName(String districtName) {
		this.districtName = districtName;
	}
	public int getFeulid() {
		return feulid;
	}
	public void setFeulid(int feulid) {
		this.feulid = feulid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getSum() {
		return sum;
	}
	public void setSum(double sum) {
		this.sum = sum;
	}
	
}
