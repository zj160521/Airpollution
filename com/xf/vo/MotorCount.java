package com.xf.vo;

public class MotorCount {
	private int province;
	private int city;
	private int town;
	private int fillyear;
	private int vehiclemodel;
	private int count;
	private double avgmile;
	private int standard;
	
	public int getProvince() {
		return province;
	}
	public void setProvince(int province) {
		this.province = province;
	}
	public int getCity() {
		return city;
	}
	public void setCity(int city) {
		this.city = city;
	}
	public int getTown() {
		return town;
	}
	public void setTown(int town) {
		this.town = town;
	}
	public int getFillyear() {
		return fillyear;
	}
	public void setFillyear(int fillyear) {
		this.fillyear = fillyear;
	}
	public int getVehiclemodel() {
		return vehiclemodel;
	}
	public void setVehiclemodel(int vehiclemodel) {
		this.vehiclemodel = vehiclemodel;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public double getAvgmile() {
		return avgmile;
	}
	public void setAvgmile(double avgmile) {
		this.avgmile = avgmile;
	}
	public int getStandard() {
		return standard;
	}
	public void setStandard(int standard) {
		this.standard = standard;
	}
	
}
